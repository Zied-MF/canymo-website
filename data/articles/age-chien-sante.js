<p>Au-delà de 10 ans, le chien entre dans le grand âge. L'objectif des soins n'est plus seulement de prévenir les maladies mais d'assurer le confort et la qualité de vie au quotidien. Les pathologies chroniques sont fréquentes et nécessitent une gestion au long cours.</p>

